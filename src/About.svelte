    </p>

    <p>
      Thanks to a certain pandemic, my education and extracurricular activities
      have moved online, and it's been a while since I and many others have
      practiced together in person. So I imagine it's difficult for those who
      are just beginning their kendo journey to pick up all the language and
      etiquette that they would normally be exposed to in the dojo. This site is
      my effort to introduce kendo's cultural nuances to these new kendoka while
      they learn to wield their shinai from home.
    </p>

