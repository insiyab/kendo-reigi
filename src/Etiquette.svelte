    <h2>~Etiquette~</h2>
    <p>
      One of my coaches is fond of saying that "kendo begins and ends with
      etiquette," a rather popular expression in the kendo world. If you've
      watched a kendo match from beginning to end, you'll notice that the two
      competitors bow to each other before the match, take a number of steps
      forward, squat with their bamboo swords drawn, before starting the match
      at the referee's signal. After the match is over, the competitors face
      each other, squat, put their swords away, back away from each other, and
      bow one more time before leaving the square. In the original Japanese, the
      words "bow" and "etiquette" happen to share the same character: 礼, which
      is pronounced "rei."
    </p>
    <p>
      Bowing is one way to show respect to our sensei (teachers) and fellow
      kendoka (kendo participants), but there’s a lot more to kendo world
      etiquette (and general Japanese etiquette), than that. How you carry
      yourself, how you interact with others, and how you practice with one
      another—all of these are influenced by etiquette.
    </p>
    <p>
      Etiquette is something that you can learn regardless of your skill level
      or age. It does take some time to learn all the rules, but keep at it and
      you’re bound to improve. And if you ever slip up on accident, keep it at
      one and don’t let it repeat into a bad habit.
    </p>
    <h3 id="carrying-yourself">Carrying yourself</h3>
    <p>
      When you head to the dojo (or sign into Zoom), strive to get there early
      so that you’re ready to start practice on time. This will give you a
      moment to wear and adjust any of your equipment and will reflect to others
      that you’re respecting their time as well. (If you do end up running late,
      quickly but quietly join the others in practice, and just be careful to
      come early next time.)
    </p>
    <p>
      Make sure to keep your back straight, chin up, paying attention to the
      sensei—the same things you would do to show a professor that you’re
      engaged in class. Slouching or leaning against the walls (or worse—leaning
      on your shinai 😱) will definitely be frowned upon! Also, say a sensei
      calls you over or directs you to stand somewhere; you should run to that
      spot, not walk. It doesn’t have to be an all-out sprint, but moving
      quickly shows that you respect the sensei’s (and everyone else’s) time.
    </p>
    <p>
      Make sure you’re treating your equipment well! For instance, you should
      not lean on your shinai (bamboo sword) or even step over it when it is
      laying on the ground (walk around it instead). A shinai represents your
      katana, a sword you use to literally strike a path forward, and you should
      treat it with respect accordingly. Every so often, you should check your
      shinai for splinters and cracks, and take care of those appropriately.
      This is done for safety (you definitely don’t want a splinter to land in
      someone’s eye), but I also see it as a sign of respect for your weapon.
    </p>
    <p>
      If you wear kendogi (kendo uniform which consists of dogi, the shirt, and
      the hakama, the trousers), make sure to fold them with care after
      practice. Some folks will toss them in a heap into their bag, and if
      you’re running late to your next stop, it’s okay to be lax on the folding
      just one time. But once you get home, take your uniform out and fold it
      properly. This will also help to maintain the crisp creases on your hakama
      (which makes it look nice for a longer period of time), but again, it’s
      also a sign of respect for your equipment.
    </p>
    <h3 id="regarding-hierarchy">Regarding hierarchy</h3>
    <p>
      In the kendo world, there is a hierarchy formed by people’s ranks in
      kendo, i.e. their skill level. For example, in the US, the term "sensei"
      is used for those who have achieved 4-dan or higher. And higher ranking
      people, like those with 7-dan or even 8-dan, garner a lot of respect on
      account of the number of years that they have devoted to kendo. When you
      speak to sensei, you should be mindful of how you say things — make sure
      that you’re being courteous, and listening carefully to what they have to
      say.
    </p>
    <p>
      When you speak to other kendoka, there’s also a hierarchy there that you
      should be mindful of. Those that are older than you are your "senpai" and
      those that are younger than you are your "kohai." Your senpai will help
      you get adjusted to kendo and teach you how to be a better kendoka, and in
      return, you should listen carefully to them like you would with your
      sensei. And when you have your own kohai, make sure to pass on what you
      learn from kendo as well! Outside of the dojo, your relationships with
      senpai and kohai may not be as formal (for instance, I’m good friends with
      many of my senpai and kohai and speak with them quite casually), but in
      the dojo, you should show your respect to them through the way you speak.
    </p>
    <p>
      So whenever you greet people, for instance, take care to greet the higher
      ranking sensei first, and then continue to greet the others (other sensei,
      and then your senpai and kohai) based on rank. When you practice with
      others, you should of course be respectful towards everyone, but
      definitely watch your etiquette around those of higher rank.
    </p>
    <h3 id="greeting-others">Greeting Others</h3>
    <p>
      When you come to practice, greet others as you see them! This is done
      typically in Japanese—we speak in Japanese because kendo etiquette is
      steeped in traditional Japanese culture, and language and culture are
      intertwined.
    </p>
    <p>
      If you’re not familiar with time-based Japanese greetings like "good
      morning," it’s perfectly okay to eschew them in favor of "please." This
      implies something along the lines of "please let me practice with you,"
      and is a rather universal way to greet people at the dojo before practice.
      When you greet people, speak loud and clear and follow the greeting with a
      slight bow (straight back, bending at the hips):
