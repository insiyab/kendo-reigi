    <h3 id="match-manners">Match Manners</h3>
    <p>
      In a practice match with your sensei or your fellow kendoka, you’ll begin
      at a "nine-step distance" from the other person. First, grip your shinai
      with your right hand, keep your back straight, and maintain eye contact
      with the other person. Take a small step forward. Loudly say
      "onegaishimasu," then bow slightly, keeping your back straight. Straighten
      yourself and take three big steps towards the other person, in sync with
      their steps. Do sonkyo (squat with your back straight, shinai held in
      front of you), then rise and begin your practice match.
    </p>
    <p>
      (It’s a "nine-step distance" because each person walks 4 steps towards
      each other, and there’s one more "step" of space between them when they do
      sonkyo.)
    </p>
    <p>
      At the end of a match (which is usually decided by the senior kendoka), do
      sonkyo once again, "sheathe" your shinai, rise, and take five and a half
      small steps backward until your feet are right next to each other. Say
      "arigatō gozaimashita" and bow slightly. After this, you can step away,
      but take care not to turn your back on your partner. I usually turn
      slightly to the side and step backwards before turning away completely.
    </p>
